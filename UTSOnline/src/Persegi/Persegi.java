package Persegi;

public class Persegi {
    
    
    public static void main(String[] args) {
        int sisi;
        int hasilKeliling;
        int hasilLuas;
        
        sisi = 31;
        
        hasilKeliling = sisi * 4;
        hasilLuas = sisi * sisi;
        
        System.out.println("Hasil Keliling Persegi : " + hasilKeliling);
        System.out.println("Hasil Luas Persegi : " + hasilLuas);
    }
}
